-- Question 5 (Marks: 3)
-- Objective: Identify seat occupancy by comparing the number of boarding passes (occupied seats) to the total seats available on an aircraft.
-- Determine Occupancy per Aircraft
-- Question Template: Display following columns flight_id, flight_no, aircraft_code, total_seats, occupied_seats, occupancy_rate_percentage

-- Write your SQL solution here:
SELECT 
    f.flight_id,
    f.flight_no,
    f.aircraft_code,
    (SELECT COUNT(*) FROM seats s WHERE s.aircraft_code = f.aircraft_code) AS total_seats,
    COUNT(bp.ticket_no) AS occupied_seats,
    ROUND(COUNT(bp.ticket_no) / (SELECT COUNT(*) FROM seats s WHERE s.aircraft_code = f.aircraft_code) * 100, 2) AS occupancy_rate_percentage
FROM flights f
LEFT JOIN boarding_passes bp ON f.flight_id = bp.flight_id
GROUP BY f.flight_id, f.flight_no, f.aircraft_code;

